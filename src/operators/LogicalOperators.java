package operators;

public class LogicalOperators {

	public static void main(String[] args) {
		//AND && / OR ++ / NOT !
		
			System.out.println((100>50) && (20>10)); //both value/condition must be true
			
			System.out.println((10 > 2) || (10 > 12)); //any one condition is true, then true
	}

}
